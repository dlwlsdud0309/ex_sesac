
public class Project_06_ex01_while {
	public static void main(String[] args) {
		
		// while문을 사용하여 1부터 10까지 출력하시오.
		// 		-> 이경우 반복횟수가 10번으로 정해져있음, 반복횟수가 정해진 경우에는 for문을 사용하면 더 직관적으로 작성이 가능하다.
		// while(조건식){
		// 		실행문장;
		// }
		
		int i = 1;
		while(i <= 10) {
			System.out.println(i);
			i++;
		}
		
	}
}
